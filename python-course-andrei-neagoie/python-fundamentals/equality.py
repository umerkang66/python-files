print("True == 1", "-->", True == 1)
print('"1" == 1', "-->", "1" == 1)
print("[] == 1", "-->", [] == 1)
print("10 == 10.00", "-->", 10 == 10.00)
print("[1, 2, 3] == [1, 2, 3]", "-->", [1, 2, 3] == [1, 2, 3])

# "is" actually checks if the location in memory where the value is stored is the same, and "==" checks the equality in value

print("True is 1", "-->", True is 1)
print('"1" is 1', "-->", "1" is 1)
print("[] is 1", "-->", [] is 1)
print("10 is 10.00", "-->", 10 is 10.00)
print("[1, 2, 3] is [1, 2, 3]", "-->", [1, 2, 3] is [1, 2, 3])
print('"umer" is "umer"', "umer" is "umer")
